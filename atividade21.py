# Exercício Python 21: Faça um programa que mostre na tela uma contagem regressiva para o estouro de
# fogos de artifício, indo de 10 até 0, com uma pausa de 1 segundo entre eles.
import time
contador = 10
while (contador):
     print("em", contador)
     contador = contador - 1
     time.sleep (1.0)
